<div class="flex flex-col">
  <h1 class="text-2xl">Product</h1>
    
  <p>GolfPad has features for clubs, players and professional golf instructors. Below is a summary of what you can expect when using the app:</p>

  <p class="text-xl mt-4">Players</p>
  <p>
    GolfPad initially comes with 5 new games for players to enjoy when playing a round. 
    Each of the games are explained below.
  </p>

  <div class="horizontal-divider-dark mt-2" />

  <p class="text-lg mt-2">'Bands'</p>
  <p>
    'Bands' is all about predicting those purple patches in golf, the times you know you are on fire. 
    Whether you start well or only get going towards the end, playing well at the right time is what counts.
  </p>
  <p class="mt-1">Rules</p>
  <p>
    Before each game players pick bands of 3 consecutive holes in which they attempt to complete a challenge. 
    The challenge categories in which you choose your 3 hole bands are:
  </p>
  <ul>
    <li>3 holes you won't lose a ball. (10 points)</li>
    <li>3 holes you don't hit a tree or enter a bunker. (15 points)</li>
    <li>3 holes where you hit 2/3 fairways. (20 points)</li>
    <li>3 holes where you hit 2/3 greens. (25 points)</li>
    <li>3 holes where you will 1 putt 2/3 greens. (30 points)</li>
    <li>3 holes you won't get a double bogey or worse. (35 points)</li>
    <li>3 holes you won't bogey or worse. (40 points)</li>
    <li>3 holes you'll be par or under. (45 points)</li>
    <li>3 holes you'll be under par. (50 points)</li>
  </ul>

  <p>This gives each player a possible total score of 270.</p>

  <p>A player cannot choose the same 3 holes for any challenge category.</p>

  <p>All challenges are adjusted using your handicap.</p>


  <div class="horizontal-divider-dark mt-2" />

  <p class="text-lg mt-2">'Mulligans'</p>
  <p>
    'Mulligans' is about utilising the free shots you get to put together the best round possible. 
    
  </p>
  <p class="mt-1">Rules</p>
  <p>Every 3 holes you get a new mulligan to use. If you win the hole you are on, you get an additional mulligan on the next hole.</p>

  <p>The rest of the round plays out as normal, with players attempting to put together the best round possible using their mulligans.</p>


  <div class="horizontal-divider-dark mt-2" />







  <p class="text-lg mt-2">'Call It'</p>
  <p>
    'Call It' is our first team game aimed at social and club groups of golfers. 
    The aim is to build a card as a team over any period of time that suits your competition. 
    When players from teams then submit their scorecards for future rounds in that period, the best scores will be used to improve the team card over that period.
  </p>

  <p>The winning team is the team with the lowest scoring card for the period of time set.</p>


  <div class="horizontal-divider-dark mt-2" />



  <p class="text-lg mt-2">'Prophet'</p>
  <p>
    'Prophet' goes a step further than Bands with predicting what will happen in your golf round. 
    Within prophet you select specific holes across a range of categories. 
    Prophet helps train a golfer to think about the individual shot and wider game in general.
  </p>

  <p>For each Prophet game, you predict the following 8 categories:</p>

  <ul class="mt-2">
    <li>
      1) Predict your exact score on each hole, you'll get 5 points on each hole that you score the correct or a better score. 
      If you get consecutive scores correct for holes then the total for that run of consecutive holes will double.
    </li>
    <li>
      2) Predict 9 holes you will get a par or better. You get 10 points for each hole you get the correct score. 
      Your score total will double everytime you get 3 correct, they don't need to be consecutive. So 3 correct is X2, 6 correct is X4 and 9 correct if X6.
    </li>
    <li>
      3) Predict 3 holes you will get a par or better. You get 25 points for each hole you get the correct score. 
      Your birdie total score will double for each birdie you get, meaning if you birdie the 3 holes you predict you would get 300 points ((25 + 25 + 25) * 4).
    </li>
    <li>
      4) Pick the hole in which you will have your best score and get 100 points if you are correct. Joint best scores count.
    </li>
    <li>
      5) Pick the hole in which you have your worst score and get 50 points if you are correct. Joint worst scores count.
    </li>
    <li>
      6) Pick 3 holes you will hit the fairway on, get 15 points for each fairway hit. Your fairway score will double for each fairway you hit from your selections in the same way as your birdie score.
    </li>
    <li>
      7) Pick 3 holes you will reach the green in regulation (handicap adjusted, assumes 2 shots per green). You will get 20 points for each green you hit.
      Your fairway score will double for each fairway you hit from your selections in the same way as your birdie score does.
    </li>
    <li>
      8) Pick 3 holes you will one putt a green. You will get 25 points for each green you hit.
      Your one putt score will double for each green you one putt from your selections in the same way as your birdie score does.
    </li>
  </ul>



  <div class="horizontal-divider-dark mt-2" />


  <p class="text-lg mt-2">'Next Up'</p>
  <p>
    'Next Up' is a game where players are assigned random holes in which they need to defend. 
    Each player is assigned a random tee box, denoting the hole in which they must win. 
    If the player wins their hole, they get 3 points, if any other player wins it, they get 1 point. 
    The winner is the player with the most points at the end of the round.
    If the number of holes is not divisible by the number of players without a remainder, the holes are divided up and the remaining holes are assigned to the lowest scoring player who can potentially win. If the leader cannot be beaten, the game ends.
  </p>


  <div class="horizontal-divider-dark mt-2" />
  <p class="mt-2">For each game type you can reuse your selections, name them and setup scenarios you want to try for different golf friend groups.</p>

  <p>
    Along with these games GolfPad provides users with all the tools expected from any golf management app. 
    Users are able to view course information and write course notes to help with their round. 
    GolfPad then goes a step further, adding new tools to record club yardages along with making hole plans for which which clubs to take and which positions to hit.
  </p>

  <p>
    Players also have the option to start tournaments with any group of friends, inside and outside of their home club. 'Build It' is the first team wide game GolfPad offers.
  </p>












  <p class=" mt-4">Along with these games we have the following features available for clubs and golf coaches:</p>

  <p class="text-xl mt-4">Clubs</p>
  <p>
    Golf clubs have the option to purchase a club setup, giving them access to all the features of the GolfPad CMS. 
    From the CMS you are able to:
  </p>
  <ul>
    <li>Manage player memberships.</li>
    <li>Manage the club diary, setting up repeatable competitions and tournaments.</li>
    <li>View your virtual club house, boards and competition results.</li>
    <li>View course records and achievements from club members.</li>
    <li>Take crypto currency payments for memberships and green fees.</li>
    <li>Setup promotions for green fee, membership and ticket sale offerings.</li>
  </ul>

  <p class="text-xl mt-2">Golf Coaches</p>
  
  <p>
    Golf coaches can use GolfPad to handle their coaching workload.
    Coaches can manage their real life diary within GolfPad, even letting students pay in crypto. 
    GolfPad also provides coaches a platform to setup subscription based content, that users also subscribe to using crypto.  
  </p>

  <p class="text-xl mt-2">Golf Societies</p>
  
  <p>
    Golf societies are a formal organisation of golfers in an area around a range of local clubs. 
    We will initially provide these societies with tools to organise their calendar, manage their members and view prior year tournaments.
  </p>

  <p>
    We have plans to introduce society specific games that will elevate this format to the next level.
  </p>

  <p>
    Longer term we will allow societies to organise as a DAO within GolfPad, enabling them to make decisions as a group using governance.
  </p>
  
  <p class="text-xl mt-2">Other Organisations</p>
  <p>As the GolfPad user base grows advertising on the platform will become more lucrative. 
    We will provide advertisers the ability to purchase discreet adspace, ensuring the DAO raises money without compromising on UX.</p>
  
  <p class="mt-2">
    Creating a golf based DAO will serve as a foundation to decentralise all the key systems that play a part in golf. 
  </p>
  

